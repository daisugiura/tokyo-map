document.addEventListener("DOMContentLoaded", () => {
  map = L.map('map').setView([35.6895, 139.6917], 12);

  L.tileLayer('https://{s}.basemaps.cartocdn.com/light_all/{z}/{x}/{y}{r}.png', {
    attribution: '&copy; OpenStreetMap & CartoDB'
  }).addTo(map);

  document.getElementById("dateSelect").addEventListener("change", updateSpots);
  updateSpots();
});

function updateSpots() {
  const date = document.getElementById("dateSelect").value;
  const spots = spotData[date] || [];

  markers.forEach(marker => map.removeLayer(marker));
  markers = [];

  const infoDiv = document.getElementById("spotInfo");
  infoDiv.innerHTML = "";

  spots.forEach(spot => {
    const marker = L.marker([spot.lat, spot.lng])
      .addTo(map)
      .bindPopup(`<strong>${spot.id}. ${spot.name}</strong><br>${spot.description}`);
    markers.push(marker);

    const card = document.createElement("div");
    card.className = "card p-4";
    card.innerHTML = `
      <img src="${spot.image}" alt="${spot.name}">
      <h2>${spot.id}. ${spot.name}</h2>
      <p>${spot.description}</p>
    `;
    infoDiv.appendChild(card);
  });

  if (spots.length > 0) {
    map.setView([spots[0].lat, spots[0].lng], 12);
  }
}
